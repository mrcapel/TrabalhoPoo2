    private void btnPesquisaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPesquisaClienteMouseClicked
        String nome = "%" + txtPesquisa.getText() + "%";
        FuncionariosDAO dao = new FuncionariosDAO();
        List<Funcionarios> lista = dao.buscaClientePorNome(nome);
        
        DefaultTableModel dados = (DefaultTableModel)tbFuncionarios.getModel();
        dados.setNumRows(0);
        
        for(Funcionarios f: lista){
            dados.addRow(new Object[]{
            f.getId(),
            f.getNome(),
            f.getRg(),
            f.getCpf(),
            f.getEmail(),
            f.getSenha(),
            f.getCargo(),
            f.getNivel_acesso(),
            f.getTelefone(),
            f.getCelular(),
            f.getCep(),
            f.getEndereco(),
            f.getNumero(),
            f.getComplemento(),
            f.getBairro(),
            f.getEstado()
        });
        }
        
    }//GEN-LAST:event_btnPesquisaClienteMouseClicked

